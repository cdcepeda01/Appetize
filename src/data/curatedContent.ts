const coverFolders: Record<CultureType, string> = {
  movie: "movies",
  book: "books",
  music: "music",
  art: "art",
};

type Seed = Omit<CultureItem, "id" | "type" | "imageUrl">;

function slug(value: string) {
  return value
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/(^-|-$)/g, "");
}

function buildItems(type: CultureType, seeds: Seed[]): CultureItem[] {
  return seeds.map((item, index) => ({
    ...item,
    id: `${type}-${slug(item.title)}`,
    type,
    coverAsset: `assets/covers/${coverFolders[type]}/${type}-${slug(item.title)}.jpg`,
    imageUrl: images[type][index % images[type].length],
  }));
}

